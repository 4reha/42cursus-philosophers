#include "philosophers.h"

void	ft_putnbr(long long nn)
{
	int	c;

	c = 0;
	if (nn >= 10)
	{
		ft_putnbr(nn / 10);
		ft_putnbr(nn % 10);
	}
	else
	{
		c = nn + 48;
		write(1, &c, 1);
	}
}
